# NUMERIC DATATYPES:-

    # 1) INT Data Type:-
        # An Integer is a number without decimal point.
        # 0 is also an integer.
        # +ve and -ve numbers can be Integers.
    #EXAMPLES:-
            # 1, 0, -45, 100, -509
            
    
    # 2) Float Data Type:-
        # A float is a number with decimal point.
        # A float can be +ve and -ve.
    #EXAMPLE:-
        # 0.0, 34.5555550, 90.50, -1.456
        
    # 3) Complex Data Type:-
        # A complex number is a combination of real number and imaginary number
            # complex = real +/- imaginary
            
            # What is mean by real number:-
                # All ints and floats are real numbers
                    # Ex: 4, -5, 9.0, -23.55, 0.56
            
            #  What is imaginary number:-
                # A number which comes with prefix 'j' is caalled imaginary number.
                    #Ex: 3j, -8j, 90.56j, -12.23j
                    
        # EXAMPLES OF COMPLEX :-
                    # 6 + 5j, -34 - 50j, 12 - 45j, 10.5 + 50.45j, -90.78 - 0.56j


